// sqrt(): It returns the square root of a number.
let sqrt = Math.sqrt(25);
console.log(sqrt); // Output: 5

// abs(): It returns the absolute value of a number.
let abs = Math.abs(-5);
console.log(abs); // Output: 5

// ceil(): It returns the smallest integer greater than or equal to a number.
let ceil = Math.ceil(4.2);
console.log(ceil); // Output: 5

// floor(): It returns the largest integer less than or equal to a number.
let floor = Math.floor(4.9);
console.log(floor); // Output: 4

// round(): It returns the value of a number rounded to the nearest integer.
let round = Math.round(4.5);
console.log(round); // Output: 5

// random(): It returns a random number between 0 (inclusive) and 1 (exclusive).
let random = Math.random();
console.log(random); // Output: 0.12345678901234567

// min(): It returns the number with the lowest value.
let min = Math.min(2, 3, 4, 5);
console.log(min); // Output: 2

// max(): It returns the number with the highest value.
let max = Math.max(2, 3, 4, 5);
console.log(max); // Output: 5

// pow(): It returns the value of a number raised to the power of another number.
let power = Math.pow(2, 3);
console.log(power); // Output: 8

