for (let i = 1; i < 5; i++) {
  switch (i) {
    case 1:
      console.log(8 + 4);
      break;
    case 2:
      console.log(8 - 4);
      break;
    case 3:
      console.log(8 / 4);
      break;
    case 4:
      console.log(8 * 4);
      break;
    default:
      console.log("Arithematic operations");
      break;
  }
}
